import java.util.*;


public class Task7 {

	public static void main(String[] args) {
		
		
		printStars(5);
		printStars(3);
		printStars(9);
		

	}
	
	private static void printStars(int num) {
		
		for(int i = 0; i<= num; i++) {
			
			System.out.print("*");
			
		}
		System.out.println();
		
	}

}
